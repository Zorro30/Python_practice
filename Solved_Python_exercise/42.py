'''
eg:
Input: 3
Output: 
---------
|...|...|
|...|...|
|...|...|
|---+---|
|...|...|
|...|...|
|...|...|
---------

Input: 1
Output: 
-----
|.|.|
|-+-|
|.|.|
-----
'''
n = int(input())
print('-'*(2*n+3))
for i in range(n):  
    print('|'+'.'*n +'|'+'.'*n+'|')
print('|'+'-'*n+'+'+'-'*n+'|')
for i in range(n):  
    print('|'+'.'*n +'|'+'.'*n+'|')
print('-'*(2*n+3))